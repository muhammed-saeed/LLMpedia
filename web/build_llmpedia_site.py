def _safe_int(x, default=0):
    try:    return int(x)
    except: return default

def slugify(title: str) -> str:
    title = title.strip().replace(" ", "_")
    return re.sub(r"[^\w\-\.\(\)]+", "_", title)

def load_articles(run_dir: Path) -> List[dict]:
    for name in ("articles.jsonl", "articles_wikitext.jsonl"):
        path = run_dir / name
        if path.exists():
            articles = []
            with path.open("r", encoding="utf-8") as f:
                for line in f:
                    line = line.strip()
                    if not line: continue
                    try:
                        obj = json.loads(line)
                        if "subject" in obj and "wikitext" in obj:
                            articles.append(obj)
                    except json.JSONDecodeError:
                        continue
            return articles
    raise FileNotFoundError(f"No articles.jsonl or articles_wikitext.jsonl in {run_dir}")


