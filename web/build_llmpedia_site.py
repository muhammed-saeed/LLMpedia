def load_funnel_data(run_dir: Path) -> Dict[str, dict]:
    candidates = [
        run_dir / "funnel_analysis" / "entity_funnel.csv",
        run_dir / "funnel_analysis" / "funnel_per_entity.csv",
    ]
    csv_path = next((p for p in candidates if p.exists()), None)
    if csv_path is None:
        print(f"  warning: No funnel data")
        return {}
    meta: Dict[str, dict] = {}
    with csv_path.open("r", encoding="utf-8") as f:
        for row in _csv.DictReader(f):
            subject = (row.get("subject") or "").strip()
            if not subject: continue
            def _i(k): return _safe_int(row.get(k, 0))
            raw_cands  = _i("raw_candidates") or _i("canonical_pre_ner_input") or _i("ner_candidates")
            canon_kept = _i("after_canonical_dedup") or _i("canonical_kept")
            meta[subject] = {
                "subject": subject, "hop": _i("hop"),
                "parent_subject": row.get("parent_subject") or None,
                "raw_candidates": raw_cands,
                "after_canonical_dedup": canon_kept,
                "rejected_canonical": max(raw_cands - canon_kept, 0),
                "ner_accepted": _i("ner_accepted"),
                "ner_rejected_not_ne": _i("ner_rejected_not_ne"),
                "ner_rejected_low_conf": _i("ner_rejected_low_conf"),
                "ner_rejected_parse": _i("ner_rejected_parse") or _i("ner_rejected_parse_fail"),
                "ner_rejected_other": _i("ner_rejected_other"),
                "rejected_by_ner": _i("rejected_by_ner") or _i("ner_rejected"),
                "enqueued_children": _i("enqueued_children") or _i("inserted_children"),
                "rejected_by_similarity": _i("rejected_by_similarity") or _i("similarity_rejected"),
            }
    print(f"  ok Funnel: {len(meta):,} entities")
    return meta


