def extract_toc(body_html):
    headings = re.findall(r'<h(\d)\s+id="([^"]+)"[^>]*>(.+?)</h\d>', body_html)
    if len(headings) < 3:
        return ""
    items = []
    for level, hid, text in headings:
        clean = re.sub(r"<[^>]+>", "", text)
        items.append(f'<li class="toc-l{level}"><a href="#{hid}">{clean}</a></li>')
    return (
        '<div class="toc" id="toc">'
        '<div class="toc-head">Contents '
        '<button class="toc-btn" onclick="this.closest(\'.toc\').classList.toggle(\'collapsed\')">'
        '[hide]</button></div>'
        '<ul>' + "\n".join(items) + '</ul></div>'
    )


