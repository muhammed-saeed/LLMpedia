    enq   = funnel["enqueued_children"]
    rej_n = funnel["rejected_by_ner"]
    rej_s = funnel["rejected_by_similarity"]

    compact = (f'<span class="fc">Raw <b>{raw}</b> → Dedup <b>{dedup}</b> '
               f'→ NER <b>{ner}</b> → Enqueued <b>{enq}</b></span>')
    parts.append('<details class="fd">')
    parts.append(f'<summary>Expansion Funnel {compact}</summary>')

    def row(l, c, e=""): return f'<div class="fr"><span>{l}</span><span class="fv">{c}{e}</span></div>'
    def sub(l): return f'<div class="fr fs"><span>{l}</span></div>'

    parts.append(row("1. Extracted", raw))
    parts.append(row("2. After dedup", dedup, f" ({_pct(dedup, raw)})"))
    parts.append(row("3. After NER", ner, f" ({_pct(ner, dedup)})"))
    if rej_n > 0:
        d = []
        for k, lbl in [("ner_rejected_not_ne","not NE"),("ner_rejected_low_conf","low conf"),
                        ("ner_rejected_parse","parse"),("ner_rejected_other","other")]:
            v = funnel.get(k, 0)
            if v: d.append(f"{lbl}: {v}")
        parts.append(sub(f'Rejected: {rej_n} ({", ".join(d)})' if d else f'Rejected: {rej_n}'))
    parts.append(row("4. Enqueued", enq, f" ({_pct(enq, ner)})"))
    if rej_s > 0: parts.append(sub(f'Similarity rejected: {rej_s}'))
    overall = _pct(enq, raw)
    if overall:
        parts.append(f'<div class="fr ft"><span><b>Overall</b></span><span class="fv"><b>{overall}</b></span></div>')
    parts.append('</details></div>')
    return "\n".join(parts)


