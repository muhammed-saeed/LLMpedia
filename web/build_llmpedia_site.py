def tokenize_for_index(text):
    toks = re.findall(r"[a-z0-9]+", text.lower())
    return [t for t in toks if len(t) >= MIN_TOKEN_LEN and t not in STOPWORDS]

def write_search_index_js(out_dir, entries, browse_order):
    titles   = [e["title"]   for e in entries]
    urls     = [e["url"]     for e in entries]
    snippets = [e["snippet"] for e in entries]
    postings = {}
    for idx, e in enumerate(entries):
        seen = set()
        for tok in tokenize_for_index(e["title"] + " " + e["snippet"][:500]):
            if tok in seen: continue
            seen.add(tok)
            postings.setdefault(tok, []).append(idx)
    payload = {"t": titles, "u": urls, "s": snippets, "p": postings,
               "o": browse_order, "chunk": BROWSE_CHUNK_SIZE, "limit": SEARCH_RESULT_LIMIT}
    js = "window.LLM_SEARCH=" + json.dumps(payload, ensure_ascii=False, separators=(",",":")) + ";\n"
    (out_dir / "search_index.js").write_text(js, encoding="utf-8")


