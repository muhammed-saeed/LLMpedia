def load_image_cache(run_dir: Path) -> Dict[str, Optional[dict]]:
    cache_path = run_dir / "gptkb_image_cache.json"
    if not cache_path.exists(): return {}
    try: raw = json.loads(cache_path.read_text(encoding="utf-8"))
    except Exception: return {}
    result: Dict[str, Optional[dict]] = {}
    for subject, entry in raw.items():
        if entry is None:
            result[subject] = None
        elif isinstance(entry, dict) and entry.get("store_path"):
            result[subject] = entry
        elif isinstance(entry, str) and entry:
            result[subject] = {"store_path": "", "image_url": entry, "source_url": entry,
                               "license": "", "artist": "", "source": "legacy"}
        else:
            result[subject] = None
    return result

def setup_images_in_site(run_dir: Path, out_dir: Path,
                         global_images: Optional[Path] = None) -> bool:
    dst = out_dir / IMAGE_STORE_DIR
    if dst.exists() or dst.is_symlink(): return True
    src = global_images if global_images else (run_dir / IMAGE_STORE_DIR)
    if not src or not src.exists() or not src.is_dir(): return False
    try:
        os.symlink(src.resolve(), dst); return True
    except (OSError, NotImplementedError): pass
    try:
        shutil.copytree(src, dst); return True
    except Exception as e:
        print(f"  warn: image link failed: {e}"); return False

def get_image_src(entry: Optional[dict]) -> Optional[str]:
    if not entry: return None
    sp = entry.get("store_path", "")
    return sp if sp else (entry.get("image_url") or None)


