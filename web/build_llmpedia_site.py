#  Infobox — NO placeholder, strips image fields, robust links
# ============================================================

def _is_image_field(key: str, val: str) -> bool:
    """Return True if this infobox field is an image reference that should be stripped."""
    key_clean = key.strip().lower().replace(" ", "_")
    # Direct key match
    if key_clean in _INFOBOX_IMAGE_KEYS:
        return True
    # Value looks like a bare image filename
    if _IMAGE_FILENAME_RE.match(val.strip()):
        return True
    # Value starts with "File:" or "Image:" (MediaWiki file references)
    val_lower = val.strip().lower()
    if val_lower.startswith("file:") or val_lower.startswith("image:"):
        return True
    # Value contains only a pixel size like "250px", "300x200px" (image_size leftover)
    if re.match(r'^\d+(\s*x\s*\d+)?\s*px$', val.strip(), re.IGNORECASE):
        return True
    return False


def extract_infobox(wikitext, slug_map, subject, image_entry=None):
    lower = wikitext.lower()
    start = lower.find("{{infobox")
    if start == -1:
        return wikitext, ""
    text = wikitext
    depth = 0; infobox_start = None; end = None; i = start
    while i < len(text) - 1:
        pair = text[i:i+2]
        if pair == "{{":
            depth += 1
            if infobox_start is None: infobox_start = i
            i += 2; continue
        if pair == "}}" and depth > 0:
            depth -= 1; i += 2
            if depth == 0: end = i; break
            continue
        i += 1
    if infobox_start is None or end is None:
        return wikitext, ""
    box_text = text[infobox_start:end]
    rest = text[:infobox_start] + text[end:]
    inner = box_text.strip()
    if inner.startswith("{{"): inner = inner[2:]
    if inner.endswith("}}"): inner = inner[:-2]
    inner = inner.strip()
    if not inner: return rest, ""
    parts = [p.strip() for p in inner.split("|") if p.strip()]
    if not parts: return rest, ""

    fields = []
    for part in parts[1:]:
        if "=" not in part: continue
        key, val = part.split("=", 1)
        key, val = key.strip(), val.strip()
        if not key or not val:
            continue
        # ── SKIP image-related fields ──
        if _is_image_field(key, val):
            continue
        fields.append((key, process_inline(val, slug_map)))
    if not fields: return rest, ""

    rows = [f'<tr><th colspan="2" class="ib-title">{html.escape(subject)}</th></tr>']

    # Image — ONLY from downloaded image cache (never from wikitext fields)
    image_src = get_image_src(image_entry)
    if image_src:
        img_html = (f'<img src="{html.escape(image_src)}" alt="{html.escape(subject)}" '
                    f'class="ib-img" loading="lazy">')
        attr_parts = []
        artist  = (image_entry or {}).get("artist", "")
        lic     = (image_entry or {}).get("license", "")
        lic_url = (image_entry or {}).get("license_url", "")
        src_url = (image_entry or {}).get("source_url", "")
        if artist:  attr_parts.append(html.escape(artist[:80]))
        if lic:
            if lic_url:
                attr_parts.append(f'<a href="{html.escape(lic_url)}" target="_blank">{html.escape(lic)}</a>')
            else:
                attr_parts.append(html.escape(lic))
        if src_url:
            attr_parts.append(f'<a href="{html.escape(src_url)}" target="_blank">source</a>')
        attr_html = f'<div class="ib-attr">{" &middot; ".join(attr_parts)}</div>' if attr_parts else ""
        rows.append(f'<tr><td colspan="2" class="ib-image">{img_html}{attr_html}</td></tr>')

    for key, val_html in fields:
        label = key.replace("_", " ").strip(": ")
        if label: label = label[0].upper() + label[1:]
        rows.append(f'<tr><th>{html.escape(label)}</th><td>{val_html}</td></tr>')

    return rest, '<table class="infobox">\n' + "\n".join(rows) + "\n</table>"


def wikitext_to_html(wikitext, slug_map):
    wikitext = re.sub(r"\{\{[^\n]+}}", "", wikitext)
    lines = wikitext.splitlines()
    html_lines = []
    for line in lines:
        stripped = line.strip()
        if not stripped:
            html_lines.append(""); continue
        m = re.match(r"^(=+)\s*(.+?)\s*\1$", stripped)
        if m:
            level = min(len(m.group(1)), 6)
            raw_heading = m.group(2).strip()
            hid = re.sub(r"[^a-z0-9]+", "-", raw_heading.lower()).strip("-")
            heading_html = process_inline(raw_heading, slug_map)
            html_lines.append(f'<h{level} id="{hid}">{heading_html}</h{level}>')
        else:
            html_lines.append(process_inline(line, slug_map))
    result = []; buffer = []
    def flush():
        nonlocal buffer
        if not buffer: return
        p = " ".join(buffer).strip()
        if not p: buffer = []; return
        result.append(p if p.startswith("<h") else f"<p>{p}</p>")
        buffer = []
    for line in html_lines:
        if not line: flush(); continue
        if line.startswith("<h"): flush(); result.append(line)
        else: buffer.append(line)
    flush()
    return "\n".join(result)


