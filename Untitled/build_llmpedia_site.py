def _fix_broken_wikilinks(text):
    length = len(text)
    opens, closes = [], []
    i = 0
    while i < length - 1:
        if text[i]=='[' and text[i+1]=='[': opens.append(i); i+=2
        elif text[i]==']' and text[i+1]==']': closes.append(i); i+=2
        else: i+=1
    used, matched = set(), set()
    for op in opens:
        for ci, cp in enumerate(closes):
            if cp > op and ci not in used: used.add(ci); matched.add(op); break
    insertions = {}
    for op in opens:
        if op in matched: continue
        cs = op+2; end = cs
        while end < length:
            if end < length-1 and text[end]==']' and text[end+1]==']': break
            if end < length-1 and text[end]=='[' and text[end+1]=='[': break
            if text[end]=='\n': break
            end += 1
        le = end
        while le > cs and text[le-1] in ' \t.,;:!?)': le -= 1
        if le > cs: insertions[le] = ']]'
        else: insertions[op] = ''
    if not insertions:
        stray = sorted([closes[ci] for ci in range(len(closes)) if ci not in used], reverse=True)
        for pos in stray: text = text[:pos] + text[pos+2:]
        return text
    parts, prev = [], 0
    for pos in sorted(insertions.keys()):
        val = insertions[pos]
        if pos==prev and val=='': parts.append(text[prev:pos]); prev=pos+2
        else: parts.append(text[prev:pos]); parts.append(val); prev=pos
    parts.append(text[prev:]); text = ''.join(parts)
    result, i, depth = [], 0, 0
    while i < len(text):
        if i<len(text)-1 and text[i]=='[' and text[i+1]=='[': depth+=1; result.append('[['); i+=2
        elif i<len(text)-1 and text[i]==']' and text[i+1]==']':
            if depth>0: depth-=1; result.append(']]')
            i+=2
        else: result.append(text[i]); i+=1
    return ''.join(result)

def process_inline(text, slug_map):
    text = _fix_broken_wikilinks(text)
    def repl_link(m):
        content = m.group(1)
        if "|" in content: target, label = content.split("|",1)
        else: target, label = content, content
        target_page, label = target.strip(), label.strip()
        if not label: label = target_page
        keys = [target_page, target_page.lower()]
        if target_page.startswith("Category:"):
            cat = target_page.split(":",1)[1].strip(); keys.extend([cat, cat.lower()])
        href = None
        for k in keys:
            if k in slug_map: href = slug_map[k]+".html"; break
        if href: return f'<a href="{href}">{html.escape(label)}</a>'
        return html.escape(label)
    text = re.sub(r"\[\[(.+?)\]\]", repl_link, text)
    text = text.replace("[[","").replace("]]","")
    text = re.sub(r"'''''(.+?)'''''", r"<b><i>\1</i></b>", text)
    text = re.sub(r"'''(.+?)'''", r"<b>\1</b>", text)
    text = re.sub(r"''(.+?)''", r"<i>\1</i>", text)
    def link_url(m):
        url = m.group(0)
        return f'<a href="{url}" target="_blank" rel="noopener noreferrer">{url}</a>'
    text = re.sub(r"https?://[^\s<]+", link_url, text)
    return text

def strip_wiki_markup(wikitext):
    wikitext = re.sub(r"\{\{Infobox[\s\S]+?\}\}", "", wikitext, flags=re.IGNORECASE)
    wikitext = re.sub(r"\{\{[^\n]+}}", "", wikitext)
    def repl(m):
        c = m.group(1); return c.split("|",1)[-1] if "|" in c else c
    wikitext = re.sub(r"\[\[(.+?)\]\]", repl, wikitext)
    wikitext = wikitext.replace("[[","").replace("]]","")
    wikitext = wikitext.replace("'''''","").replace("'''","").replace("''","")
    return re.sub(r"\s+", " ", wikitext).strip()

