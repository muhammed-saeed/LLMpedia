def build_site_for_run(run_dir, clean=False, no_images=False, workers=DEFAULT_WORKERS,
                       mode="deploy", global_images=None, root_prefix=".."):
    anonymous = (mode=="anonymous")
    out_dir = run_dir / "site"
    if clean and out_dir.exists(): print(f"  Cleaning {out_dir}"); shutil.rmtree(out_dir)
    out_dir.mkdir(parents=True, exist_ok=True)
    print(f"  Loading articles...")
    articles = load_articles(run_dir)
    print(f"    {len(articles):,} articles")
    funnel_map = load_funnel_data(run_dir)
    run_meta = load_run_meta(run_dir)
    model_display = extract_model_display(run_meta)
    model_logo_fn = get_model_logo_filename(run_meta)
    print(f"    Model: {model_display or '?'}, Logo: {model_logo_fn or 'none'}")
    slug_map = build_slug_map(articles); parent_map = build_parent_map(articles)
    write_style_css(out_dir)
    logos = copy_logos_to_site(out_dir)
    model_logo_path = copy_model_logo_to_site(out_dir, model_logo_fn)
    site_logo_path = copy_site_logo(out_dir)
    nav_html = _nav_html(model_display, model_logo_path, root_prefix, site_logo_path)
    build_static_pages(out_dir, logos, anonymous, nav_html)
    footer_html = build_footer_html(logos, anonymous)
    image_cache = {}
    if not no_images:
        image_cache = load_image_cache(run_dir)
        images_ok = setup_images_in_site(run_dir, out_dir, global_images)
        found = sum(1 for v in image_cache.values() if v is not None)
        if image_cache: print(f"  Images: {found:,}/{len(image_cache):,}{' (linked)' if images_ok else ''}")
    entries = []
    for a in articles:
        subject = a["subject"]; slug = slug_map[subject]
        plain = strip_wiki_markup(a["wikitext"])
        snippet = plain[:250]+("..." if len(plain)>250 else "")
        entries.append({"title":subject,"url":f"{slug}.html","snippet":snippet})
    indexed = list(enumerate(entries))
    indexed.sort(key=lambda it: (parent_map.get(it[1]["title"],{}).get("hop",0), it[1]["title"].lower()))
    browse_order = [i for i,_ in indexed]
    existing_html = set()
    if not clean:
        for f in out_dir.iterdir():
            if f.suffix==".html" and f.name not in ("index.html","data_protection.html","imprint.html","accessibility.html"):
                existing_html.add(f.stem)
    to_build = [a for a in articles if slug_map[a["subject"]] not in existing_html]
    skipped = len(articles)-len(to_build)
    if skipped: print(f"  Skipping {skipped:,} existing (--clean to rebuild)")
    print(f"  Building {len(to_build):,} pages (workers={workers})...")
    t0 = time.perf_counter(); built = 0
    if len(to_build)<50 or workers<=1:
        for a in to_build:
            make_article_page(a, slug_map, parent_map, funnel_map, logos, image_cache, out_dir, footer_html, nav_html, model_display, model_logo_path, anonymous)
            built += 1
            if built%10000==0: print(f"    {built:,}/{len(to_build):,}...")
    else:
        with ThreadPoolExecutor(max_workers=workers) as pool:
            futs = {pool.submit(make_article_page, a, slug_map, parent_map, funnel_map, logos, image_cache, out_dir, footer_html, nav_html, model_display, model_logo_path, anonymous): a["subject"] for a in to_build}
            for fut in as_completed(futs):
                try: fut.result()
                except Exception as e: print(f"    WARN: {futs[fut]}: {e}")
                built += 1
                if built%10000==0: print(f"    {built:,}/{len(to_build):,}...")
    elapsed = time.perf_counter()-t0
    print(f"  Built {built:,} in {elapsed:.1f}s ({built/elapsed:.0f}/sec)" if elapsed>0 else f"  Built {built:,}")
    write_search_index_js(out_dir, entries, browse_order)
    make_index_page(entries, articles, slug_map, parent_map, funnel_map, logos, out_dir, nav_html, anonymous, model_display, model_logo_path)
    return out_dir

def find_run_dirs(root):
    if not root.is_dir(): return []
    for n in ("articles.jsonl","articles_wikitext.jsonl"):
        if (root/n).exists(): return [root]
    found = set()
    for n in ("articles.jsonl","articles_wikitext.jsonl"):
        for p in root.rglob(n): found.add(p.parent)
    return sorted(found)

def main():
    parser = argparse.ArgumentParser(description="Build LLMpedia static site")
    parser.add_argument("root_path")
    parser.add_argument("--clean", action="store_true")
    parser.add_argument("--only-first", action="store_true")
    parser.add_argument("--no-images", action="store_true")
    parser.add_argument("--mode", choices=["anonymous","deploy"], default="deploy")
    parser.add_argument("--workers", type=int, default=DEFAULT_WORKERS)
    parser.add_argument("--global-images", type=str, default=None)
    parser.add_argument("--root-prefix", type=str, default="..",
                        help="Relative path from this site to the deploy root (default: ..)")
    args = parser.parse_args()
    root = Path(args.root_path).expanduser().resolve()
    if not root.exists() or not root.is_dir(): raise SystemExit(f"Not a directory: {root}")
    run_dirs = find_run_dirs(root)
    if not run_dirs: raise SystemExit(f"No articles found under: {root}")
    if args.only_first: run_dirs = run_dirs[:1]
    global_images = Path(args.global_images).resolve() if args.global_images else None
    print(f"Found {len(run_dirs)} run(s)  [mode={args.mode}, root-prefix={args.root_prefix}]")
    for rd in run_dirs: print(f"  - {rd}")
    print()
    for rd in run_dirs:
        print(f"[BUILD] {rd}")
        try:
            out = build_site_for_run(rd, args.clean, args.no_images, args.workers,
                                     args.mode, global_images, args.root_prefix)
            print(f"[OK]   {out / 'index.html'}\n")
        except SystemExit: raise
        except Exception as e:
            import traceback; traceback.print_exc(); print(f"[FAIL] {e}\n")

if __name__ == "__main__":
    main()