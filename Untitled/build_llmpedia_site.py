def _is_image_field(key, val):
    """Return True if this infobox field is an image reference to strip."""
    key_clean = key.strip().lower().replace(" ","_")
    if key_clean in _INFOBOX_IMAGE_KEYS: return True
    val_clean = val.strip()
    if _IMAGE_FILENAME_RE.match(val_clean): return True
    vl = val_clean.lower()
    if vl.startswith("file:") or vl.startswith("image:"): return True
    if re.match(r'^\d+(\s*x\s*\d+)?\s*px$', val_clean, re.IGNORECASE): return True
    return False


def _split_respecting_braces(text, sep="|"):
    """Split text on `sep` but NOT inside {{ }} or [[ ]] nesting."""
    parts = []
    current = []
    depth_curly = 0
    depth_square = 0
    i = 0
    while i < len(text):
        c = text[i]
        # Track {{ }}
        if i < len(text) - 1 and text[i:i+2] == "{{":
            depth_curly += 1; current.append("{{"); i += 2; continue
        if i < len(text) - 1 and text[i:i+2] == "}}":
            depth_curly = max(0, depth_curly - 1); current.append("}}"); i += 2; continue
        # Track [[ ]]
        if i < len(text) - 1 and text[i:i+2] == "[[":
            depth_square += 1; current.append("[["); i += 2; continue
        if i < len(text) - 1 and text[i:i+2] == "]]":
            depth_square = max(0, depth_square - 1); current.append("]]"); i += 2; continue
        # Split only at top level
        if c == sep and depth_curly == 0 and depth_square == 0:
            parts.append("".join(current))
            current = []
        else:
            current.append(c)
        i += 1
    parts.append("".join(current))
    return parts


def _resolve_template(tpl_text):
    """Resolve a single {{template|args...}} into plain text.
    Handles: ubl, unbulleted list, plainlist, flatlist, hlist,
    start date, end date, birth date, death date, nowrap, small,
    URL, convert, and more. Unknown templates → stripped."""
    # Remove outer {{ }}
    inner = tpl_text.strip()
    if inner.startswith("{{"): inner = inner[2:]
    if inner.endswith("}}"): inner = inner[:-2]
    inner = inner.strip()
    if not inner: return ""

    # Split template name from args (respecting nesting)
    parts = _split_respecting_braces(inner, "|")
    name = parts[0].strip().lower()
    args = [p.strip() for p in parts[1:]]

    # ── List templates: {{ubl|a|b|c}} → "a, b, c" ──
    if name in ("ubl", "unbulleted list", "plainlist", "flatlist",
                "hlist", "bulleted list", "cslist", "collapsible list"):
        items = [_resolve_templates_in_text(a) for a in args if a.strip() and "=" not in a.split("}", 1)[0]]
        return ", ".join(i for i in items if i)

    # ── Date templates: {{start date and age|1916|3|7}} → "7 March 1916" ──
    month_names = ["","January","February","March","April","May","June",
                   "July","August","September","October","November","December"]
    if name in ("start date", "start date and age", "birth date",
                "birth date and age", "death date", "death date and age",
                "end date", "end date and age", "dob", "dod",
                "birth-date and age", "death-date and age"):
        # Filter out named params like df=y, mf=y
        nums = [a for a in args if a.strip() and "=" not in a]
        try:
            year = nums[0] if len(nums) > 0 else ""
            month = int(nums[1]) if len(nums) > 1 else 0
            day = nums[2] if len(nums) > 2 else ""
            mname = month_names[month] if 0 < month < 13 else str(month)
            if day and mname and year:
                return f"{day} {mname} {year}"
            elif mname and year:
                return f"{mname} {year}"
            elif year:
                return str(year)
        except (ValueError, IndexError):
            return " ".join(nums) if nums else ""

    # ── Formatting templates ──
    if name in ("nowrap", "nobr", "nobreak"):
        return _resolve_templates_in_text(args[0]) if args else ""
    if name in ("small", "smaller", "big", "bigger", "large"):
        return _resolve_templates_in_text(args[0]) if args else ""
    if name in ("lang", "transl", "transliteration"):
        # {{lang|de|München}} → "München"
        return _resolve_templates_in_text(args[1]) if len(args) > 1 else (_resolve_templates_in_text(args[0]) if args else "")
    if name in ("native name", "native_name"):
        # {{native name|de|Bayerische Motoren Werke}} → "Bayerische Motoren Werke"
        return _resolve_templates_in_text(args[1]) if len(args) > 1 else ""

    # ── Currency / number templates ──
    if name in ("currency", "us$", "usd", "eur", "gbp", "jpy", "cny"):
        return _resolve_templates_in_text(args[0]) if args else ""
    if name in ("convert",):
        # {{convert|100|km|mi}} → "100 km"
        if len(args) >= 2:
            return f"{args[0]} {args[1]}"
        return args[0] if args else ""
    if name in ("increase", "decrease", "steady"):
        sym = {"increase": "▲", "decrease": "▼", "steady": "▬"}.get(name, "")
        return f"{sym} {_resolve_templates_in_text(args[0])}" if args else sym

    # ── URL template ──
    if name in ("url", "official website", "official url"):
        return args[0] if args else ""

    # ── Coord template → just show text ──
    if name.startswith("coord"):
        nums = [a for a in args if a.strip() and "=" not in a]
        return ", ".join(nums[:4]) + "..." if len(nums) > 4 else ", ".join(nums)

    # ── Flag/country templates ──
    if name in ("flag", "flagicon", "flagcountry", "flag icon"):
        return args[0] if args else ""
    if name.startswith("flag") and args:
        return args[0]

    # ── Color/color box → skip ──
    if name in ("color", "color box", "colorbox", "legend", "col-begin", "col-end"):
        return ""

    # ── Info/citation templates → skip silently ──
    if name.startswith("cite") or name.startswith("ref") or name in ("sfn", "harv", "efn"):
        return ""

    # ── If we have simple args, join them ──
    clean_args = [a for a in args if a.strip() and "=" not in a.split("}", 1)[0]]
    if clean_args and len(name) < 30:
        # Short template name + args → likely a list or value
        resolved = [_resolve_templates_in_text(a) for a in clean_args]
        return ", ".join(r for r in resolved if r)

    # ── Unknown template with no useful args → empty ──
    return ""


def _resolve_templates_in_text(text):
    """Recursively resolve all {{...}} templates in a string."""
    if "{{" not in text:
        return text.strip()

    result = []
    i = 0
    while i < len(text):
        if i < len(text) - 1 and text[i:i+2] == "{{":
            # Find matching }}
            depth = 1
            j = i + 2
            while j < len(text) - 1 and depth > 0:
                if text[j:j+2] == "{{": depth += 1; j += 2
                elif text[j:j+2] == "}}": depth -= 1; j += 2
                else: j += 1
            if depth == 0:
                tpl = text[i:j]
                resolved = _resolve_template(tpl)
                result.append(resolved)
                i = j
            else:
                # Unclosed {{ — skip it
                i += 2
        else:
            result.append(text[i])
            i += 1
    return "".join(result).strip()


def _clean_infobox_value(raw_val):
    """Clean a raw infobox value: resolve templates, strip garbage."""
    val = _resolve_templates_in_text(raw_val)
    # Remove HTML comments
    val = re.sub(r"<!--.*?-->", "", val)
    # Remove <ref>...</ref> tags
    val = re.sub(r"<ref[^>]*>.*?</ref>", "", val, flags=re.DOTALL | re.IGNORECASE)
    val = re.sub(r"<ref[^/]*/\s*>", "", val, flags=re.IGNORECASE)
    # Remove <br>, <br/> → comma
    val = re.sub(r"<br\s*/?\s*>", ", ", val, flags=re.IGNORECASE)
    # Collapse whitespace
    val = re.sub(r"\s+", " ", val).strip()
    # Strip leading/trailing commas and pipes
    val = val.strip(",|; ").strip()
    return val


def _is_garbage_value(val):
    """Return True if infobox value is garbage that should be hidden."""
    v = val.strip()
    if not v: return True
    if len(v) < 2: return True
    # Unresolved template remnants
    if v.startswith("{{") or v.endswith("}}"):
        return True
    # Just a number with px (size field)
    if re.match(r'^\d+(\s*x\s*\d+)?\s*px$', v, re.IGNORECASE):
        return True
    # Just "yes", "no", "y", "n" (toggle fields)
    if v.lower() in ("yes", "no", "y", "n", "true", "false"):
        return True
    return False


def extract_infobox(wikitext, slug_map, subject, image_entry=None):
    """Extract and render infobox from wikitext. Uses template-aware parsing."""
    lower = wikitext.lower()
    start = lower.find("{{infobox")
    if start == -1: return wikitext, ""

    # Find matching }} for the infobox (respecting nesting)
    text = wikitext
    depth = 0; ib_start = None; end = None; i = start
    while i < len(text) - 1:
        pair = text[i:i+2]
        if pair == "{{":
            depth += 1
            if ib_start is None: ib_start = i
            i += 2; continue
        if pair == "}}" and depth > 0:
            depth -= 1; i += 2
            if depth == 0: end = i; break
            continue
        i += 1
    if ib_start is None or end is None: return wikitext, ""

    box_text = text[ib_start:end]
    rest = text[:ib_start] + text[end:]

    # Strip outer {{ }}
    inner = box_text.strip()
    if inner.startswith("{{"): inner = inner[2:]
    if inner.endswith("}}"): inner = inner[:-2]
    inner = inner.strip()
    if not inner: return rest, ""

    # Split on | RESPECTING nested {{ }} and [[ ]]
    parts = _split_respecting_braces(inner, "|")
    if not parts: return rest, ""

    # First part is "Infobox XYZ" — skip it
    # Remaining parts are "key = value"
    fields = []
    for part in parts[1:]:
        if "=" not in part:
            continue
        key, val = part.split("=", 1)
        key = key.strip()
        raw_val = val.strip()
        if not key or not raw_val:
            continue
        # Skip image-related fields
        if _is_image_field(key, raw_val):
            continue
        # Clean and resolve templates
        cleaned = _clean_infobox_value(raw_val)
        if _is_garbage_value(cleaned):
            continue
        # Process wikilinks
        html_val = process_inline(cleaned, slug_map)
        # Final check: if after HTML processing it's empty or just whitespace
        text_only = re.sub(r"<[^>]+>", "", html_val).strip()
        if not text_only or len(text_only) < 2:
            continue
        fields.append((key, html_val))

    if not fields: return rest, ""

    # Build HTML table
    rows = [f'<tr><th colspan="2" class="ib-title">{html.escape(subject)}</th></tr>']

    # Image — ONLY from downloaded image cache (never from wikitext fields)
    image_src = get_image_src(image_entry)
    if image_src:
        img_html = (f'<img src="{html.escape(image_src)}" alt="{html.escape(subject)}" '
                    f'class="ib-img" loading="lazy">')
        attr_parts = []
        artist  = (image_entry or {}).get("artist", "")
        lic     = (image_entry or {}).get("license", "")
        lic_url = (image_entry or {}).get("license_url", "")
        src_url = (image_entry or {}).get("source_url", "")
        if artist:  attr_parts.append(html.escape(artist[:80]))
        if lic:
            if lic_url:
                attr_parts.append(f'<a href="{html.escape(lic_url)}" target="_blank">{html.escape(lic)}</a>')
            else:
                attr_parts.append(html.escape(lic))
        if src_url:
            attr_parts.append(f'<a href="{html.escape(src_url)}" target="_blank">source</a>')
        attr_html = f'<div class="ib-attr">{" &middot; ".join(attr_parts)}</div>' if attr_parts else ""
        rows.append(f'<tr><td colspan="2" class="ib-image">{img_html}{attr_html}</td></tr>')

    for key, val_html in fields:
        label = key.replace("_", " ").strip(": ")
        if label:
            label = label[0].upper() + label[1:]
        rows.append(f'<tr><th>{html.escape(label)}</th><td>{val_html}</td></tr>')

    return rest, '<table class="infobox">\n' + "\n".join(rows) + "\n</table>"

def _strip_all_templates(text):
    """Fast removal of all {{...}} templates (handles nesting). For body text."""
    if "{{" not in text:
        return text
    prev = None
    while prev != text and "{{" in text:
        prev = text
        text = re.sub(r"\{\{[^{}]*\}\}", "", text)
    return text

# ── Heuristic heading splitter for broken == headings (common in Llama) ──

_HEADING_CONNECTORS = {"and","of","the","for","in","on","at","to","by","with","from","or","a","an"}
_SENTENCE_STARTERS = {"the","a","an","in","this","it","its","as","these","there","they",
                      "he","she","we","his","her","during","after","before","since",
                      "throughout","following","over","between","although","however",
                      "under","through","according","many","most","some","several"}

def _split_broken_heading(text_after_equals, subject=""):
    """Split 'Title Body text...' into (heading, body) using heuristics."""
    words = text_after_equals.split()
    if not words:
        return "", "", False
    subj_words = subject.lower().split() if subject else []
    best = len(words)
    for i in range(1, min(8, len(words))):
        rest = words[i:]
        if not rest: continue
        first = rest[0]; fl = first.lower()
        hit = False
        # Subject name starts body
        if subj_words and fl == subj_words[0]:
            cl = min(len(subj_words), len(rest))
            if " ".join(w.lower() for w in rest[:cl]) == " ".join(subj_words[:cl]):
                hit = True
        # Common sentence starter
        if fl in _SENTENCE_STARTERS and i >= 1:
            hit = True
        # Capital word + lowercase next word (e.g. "GlobalFoundries offers")
        # But NOT if the lowercase word is a heading connector (e.g. "Career and Legacy")
        if (len(rest) >= 2 and first[0].isupper() and rest[1][0].islower()
                and fl not in _HEADING_CONNECTORS
                and rest[1].lower() not in _HEADING_CONNECTORS
                and i >= 1):
            hit = True
        # After 6 words, force split
        if i >= 6 and first[0].isupper():
            hit = True
        if hit:
            best = i; break
    return " ".join(words[:best]), " ".join(words[best:]), True

def wikitext_to_html(wikitext, slug_map, subject=""):
    """Convert wikitext to HTML. Returns (html_str, used_heuristic)."""
    wikitext = _strip_all_templates(wikitext)
    lines = wikitext.splitlines()
    html_lines = []
    heuristic_used = False
    for line in lines:
        stripped = line.strip()
        if not stripped: html_lines.append(""); continue
        # 1. Standard heading: == Title ==
        m = re.match(r"^(=+)\s*(.+?)\s*\1$", stripped)
        if m:
            level = min(len(m.group(1)), 6)
            raw = m.group(2).strip()
            hid = re.sub(r"[^a-z0-9]+", "-", raw.lower()).strip("-")
            html_lines.append(f'<h{level} id="{hid}">{process_inline(raw, slug_map)}</h{level}>')
            continue
        # 2. Broken heading: starts with == but no closing ==
        m2 = re.match(r"^(=+)\s+(.+)$", stripped)
        if m2 and not stripped.rstrip().endswith("="):
            level = min(len(m2.group(1)), 6)
            rest_text = m2.group(2).strip()
            heading_text, body_text, _ = _split_broken_heading(rest_text, subject)
            if heading_text:
                heuristic_used = True
                hid = re.sub(r"[^a-z0-9]+", "-", heading_text.lower()).strip("-")
                dot = '<span class="h-heuristic" title="Section boundary detected by heuristic">●</span> '
                html_lines.append(f'<h{level} id="{hid}">{dot}{process_inline(heading_text, slug_map)}</h{level}>')
                if body_text:
                    html_lines.append(process_inline(body_text, slug_map))
                continue
        # 3. Normal text
        html_lines.append(process_inline(stripped, slug_map))

    result, buffer = [], []
    def flush():
        nonlocal buffer
        if not buffer: return
        p = " ".join(buffer).strip()
        if not p: buffer = []; return
        result.append(p if p.startswith("<h") else f"<p>{p}</p>")
        buffer = []
    for line in html_lines:
        if not line: flush(); continue
        if line.startswith("<h"): flush(); result.append(line)
        else: buffer.append(line)
    flush()
    body_html = "\n".join(result)
    if heuristic_used:
        body_html += ('\n<div class="h-note"><span class="h-heuristic">●</span> '
                      'Some section boundaries were detected using heuristics. '
                      'Certain LLMs occasionally produce headings without standard '
                      'wikitext closing markers, which are resolved automatically.</div>')
    return body_html, heuristic_used

def extract_toc(body_html):
    headings = re.findall(r'<h(\d)\s+id="([^"]+)"[^>]*>(.+?)</h\d>', body_html)
    if len(headings)<3: return ""
    items = [f'<li class="toc-l{l}"><a href="#{h}">{re.sub(r"<[^>]+>","",t)}</a></li>' for l,h,t in headings]
    return ('<div class="toc" id="toc"><div class="toc-head">Contents '
            '<button class="toc-btn" onclick="this.closest(\'.toc\').classList.toggle(\'collapsed\')">[hide]</button></div>'
            '<ul>'+"\n".join(items)+'</ul></div>')

